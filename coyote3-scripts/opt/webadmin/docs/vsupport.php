<p>Vortech Support Option</p>
<p>Enabling this option will permit Vortech Consulting authorized hosts to connect to your firewall for support purposes. The specific changes which will be made to the firewall include:</p>
<p>- Authorized network addresses will be added to the SSH and Web Administrator access control lists<br>
  - Vortech Consulting's SSH public key will be added to the authorized list of SSH administrators.</p>
<p>This option should only be used during a support or debugging session with authorized Vortech Consulting support personel. Once the support session is concluded, this option should be disabled. </p>
